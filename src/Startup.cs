using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.SpaServices.Webpack;
using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using SnappetChallenge.Core;
using SnappetChallenge.Providers;
using SnappetChallenge.Repositories;

namespace SnappetChallenge
{
    public class Startup
    {
        public Startup(IConfiguration configuration)
        {
            Configuration = configuration;
        }

        public IConfiguration Configuration { get; }

        // This method gets called by the runtime. Use this method to add services to the container.
        public void ConfigureServices(IServiceCollection services)
        {
            services.AddMvc();
            services.AddDbContext<SnappetDbContext>(x => x.UseInMemoryDatabase(ApplicationConstants.DATABASE_NAME));

            //These service only is being used for injecting initial data to the SnappetDbContext
            services.AddTransient<IJsonFileDeserilizer, JsonFileDeserilizer>();
            services.AddSingleton<IWorkDataProvider>(x => new WorkDataProvider(ApplicationConstants.WORK_DATA_JSON_FILE_PATH, x.GetService<IJsonFileDeserilizer>()));

            services.AddSingleton<IClock, Clock>();
            services.AddScoped<IClassProgressRepository, ClassProgressRepository>();
        }

        // This method gets called by the runtime. Use this method to configure the HTTP request pipeline.
        public void Configure(IApplicationBuilder app, IHostingEnvironment env)
        {
            if (env.IsDevelopment())
            {
                app.UseDeveloperExceptionPage();
                app.UseWebpackDevMiddleware(new WebpackDevMiddlewareOptions
                {
                    HotModuleReplacement = true,
                    ReactHotModuleReplacement = true
                });
            }
            else
            {
                app.UseExceptionHandler("/Home/Error");
            }

            app.UseStaticFiles();

            app.UseMvc(routes =>
            {
                routes.MapRoute(
                    name: "default",
                    template: "{controller=Home}/{action=Index}/{id?}");

                routes.MapSpaFallbackRoute(
                    name: "spa-fallback",
                    defaults: new { controller = "Home", action = "Index" });
            });
        }
    }
}
